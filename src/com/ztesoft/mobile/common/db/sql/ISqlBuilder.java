public interface ISqlBuilder {

	/**
	 * build update sql header
	 * 
	 * @param buffer
	 * @param tableName
	 */
	public  String buildHeaderSql(String tableName);

	/**
	 * build update sql column
	 * 
	 * @param buffer
	 * @param columnPatternStrArr
	 */
	public  String buildColumnSql(String[] columnPatternStrArr);

	/**
	 * build update sql where
	 * 
	 * @param buffer
	 * @param wherePatternStrArr
	 */
	public  String buildWhereColumnSql(String[] wherePatternStrArr);

}