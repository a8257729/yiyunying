package com.ztesoft.mobile.v2.entity.workform.shanghai;

import com.ztesoft.mobile.v2.core.Entity;


public class WorkOrderDetail extends Entity {
	public static final String WORK_ORDER_DETAIL_LIST_NODE = "workOrderDetailList";
	public static final String WORK_ORDER_DETAIL_NODE = "workOrderDetail";
	
	private String name;
	private String value;
	
	public WorkOrderDetail() {
		
	}
	
	public WorkOrderDetail(String name, String value) {
		this.name = name;
		this.value = value;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
